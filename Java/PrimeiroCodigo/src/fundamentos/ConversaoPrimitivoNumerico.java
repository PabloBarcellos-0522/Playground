package fundamentos;

public class ConversaoPrimitivoNumerico
{
    public static void main(String[] args)
    {
        // Conversão explicita (CAST)
        float a = (float) 1.123456789101112131415;
        System.out.println(a);

        int b = 5;
        // Conversão explicita (CAST)
        byte c = (byte) b;
        System.out.println(c);
    }
}
